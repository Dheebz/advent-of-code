"""RPG Simulator 20XX -- Advent of Code 2015 Day 21."""

from __future__ import annotations

import argparse
from itertools import combinations
from pathlib import Path
from typing import Iterable, Tuple

Player = Tuple[int, int, int]  # hp, damage, armor

WEAPONS = [
    ("Dagger", 8, 4, 0),
    ("Shortsword", 10, 5, 0),
    ("Warhammer", 25, 6, 0),
    ("Longsword", 40, 7, 0),
    ("Greataxe", 74, 8, 0),
]

ARMOR = [
    ("None", 0, 0, 0),
    ("Leather", 13, 0, 1),
    ("Chainmail", 31, 0, 2),
    ("Splintmail", 53, 0, 3),
    ("Bandedmail", 75, 0, 4),
    ("Platemail", 102, 0, 5),
]

RINGS = [
    ("Damage +1", 25, 1, 0),
    ("Damage +2", 50, 2, 0),
    ("Damage +3", 100, 3, 0),
    ("Defense +1", 20, 0, 1),
    ("Defense +2", 40, 0, 2),
    ("Defense +3", 80, 0, 3),
]


def parse_input(raw: str) -> Player:
    """Parse boss stats into an (hp, damage, armor) tuple."""
    lines = raw.strip().splitlines()
    hp = int(lines[0].split(": ")[1])
    damage = int(lines[1].split(": ")[1])
    armor = int(lines[2].split(": ")[1])
    return hp, damage, armor


def player_wins(player: Player, boss: Player) -> bool:
    """Determine if the player wins against the boss."""
    php, pdamage, parmor = player
    bhp, bdamage, barmor = boss

    p_hit = max(1, pdamage - barmor)
    b_hit = max(1, bdamage - parmor)

    rounds_to_kill_boss = (bhp + p_hit - 1) // p_hit
    rounds_to_kill_player = (php + b_hit - 1) // b_hit
    return rounds_to_kill_boss <= rounds_to_kill_player


def all_loadouts() -> Iterable[tuple[int, Player]]:
    """Generate all valid equipment combinations with their costs."""
    for _, weapon_cost, weapon_damage, weapon_armor in WEAPONS:
        for _, armor_cost, _, armor_rating in ARMOR:
            for ring_count in range(3):
                for rings in combinations(RINGS, ring_count):
                    ring_cost = sum(ring[1] for ring in rings)
                    ring_damage = sum(ring[2] for ring in rings)
                    ring_armor = sum(ring[3] for ring in rings)
                    total_cost = weapon_cost + armor_cost + ring_cost
                    damage = weapon_damage + ring_damage
                    armor = weapon_armor + armor_rating + ring_armor
                    yield total_cost, (100, damage, armor)


def solve_part_one(boss: Player) -> int:
    """Return the minimum cost to defeat the boss."""
    best = float("inf")
    for cost, player in all_loadouts():
        if player_wins(player, boss):
            best = min(best, cost)
    return int(best)


def solve_part_two(boss: Player) -> int:
    """Return the maximum cost while still losing to the boss."""
    worst = 0
    for cost, player in all_loadouts():
        if not player_wins(player, boss):
            worst = max(worst, cost)
    return worst

def _infer_year_day(path: Path) -> tuple[int | None, int | None]:
    """
    Infer (year, day) from a filename like 'solution_2015_day_01.py'.

    Returns (None, None) if it can't be parsed.
    """
    name = path.name  # e.g. 'solution_2015_day_01.py'

    # Strip extension
    if name.endswith(".py"):
        base = name[:-3]
    else:
        base = name

    parts = base.split("_")
    # Expected: ["solution", "2015", "day", "01"]
    if len(parts) != 4 or parts[0] != "solution" or parts[2] != "day":
        return None, None

    try:
        year = int(parts[1])
        day = int(parts[3])
    except ValueError:
        return None, None

    return year, day

