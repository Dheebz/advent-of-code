"""Aunt Sue -- Advent of Code 2015 Day 16."""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Callable, Dict, List

TARGET = {
    "children": 3,
    "cats": 7,
    "samoyeds": 2,
    "pomeranians": 3,
    "akitas": 0,
    "vizslas": 0,
    "goldfish": 5,
    "trees": 3,
    "cars": 2,
    "perfumes": 1,
}


def parse_input(raw: str) -> List[Dict[str, int]]:
    """Parse Aunt Sue property descriptions."""
    sues: List[Dict[str, int]] = []
    for line in raw.strip().splitlines():
        _, rest = line.split(": ", 1)
        properties = {}
        for part in rest.split(", "):
            name, value = part.split(": ")
            properties[name] = int(value)
        sues.append(properties)
    return sues


def matches_part_one(sue: Dict[str, int]) -> bool:
    """Check if the Sue matches target properties using equality."""
    return all(TARGET[k] == v for k, v in sue.items())


def matches_part_two(sue: Dict[str, int]) -> bool:
    """Check if the Sue matches target properties using ranged rules."""
    for k, v in sue.items():
        target = TARGET[k]
        if k in {"cats", "trees"}:
            if not v > target:
                return False
        elif k in {"pomeranians", "goldfish"}:
            if not v < target:
                return False
        else:
            if v != target:
                return False
    return True


def find_sue(sues: List[Dict[str, int]], matcher: Callable[[Dict[str, int]], bool]) -> int:
    """Return the 1-based index of the Sue matching the predicate."""
    for idx, sue in enumerate(sues, start=1):
        if matcher(sue):
            return idx
    return -1


def solve_part_one(sues: List[Dict[str, int]]) -> int:
    """Return the index of the matching Sue using equality rules."""
    return find_sue(sues, matches_part_one)


def solve_part_two(sues: List[Dict[str, int]]) -> int:
    """Return the index of the matching Sue using ranged rules."""
    return find_sue(sues, matches_part_two)

def _infer_year_day(path: Path) -> tuple[int | None, int | None]:
    """
    Infer (year, day) from a filename like 'solution_2015_day_01.py'.

    Returns (None, None) if it can't be parsed.
    """
    name = path.name  # e.g. 'solution_2015_day_01.py'

    # Strip extension
    if name.endswith(".py"):
        base = name[:-3]
    else:
        base = name

    parts = base.split("_")
    # Expected: ["solution", "2015", "day", "01"]
    if len(parts) != 4 or parts[0] != "solution" or parts[2] != "day":
        return None, None

    try:
        year = int(parts[1])
        day = int(parts[3])
    except ValueError:
        return None, None

    return year, day

