"""Corporate Policy -- Advent of Code 2015 Day 11."""

from __future__ import annotations

import argparse
from pathlib import Path

FORBIDDEN = set("iol")


def parse_input(raw: str) -> str:
    """Return the starting password.

    Args:
        raw (str): Raw puzzle input.

    Returns:
        str: Trimmed password.
    """
    return raw.strip()


def increment(password: str) -> str:
    """Increment the password with wraparound.

    Args:
        password (str): Current password.

    Returns:
        str: Next candidate password.
    """
    chars = list(password)
    i = len(chars) - 1
    while i >= 0:
        next_char = chr(((ord(chars[i]) - ord("a") + 1) % 26) + ord("a"))
        chars[i] = next_char
        if next_char != "a":
            break
        i -= 1
    return "".join(chars)


def is_valid(password: str) -> bool:
    """Check whether the password meets the corporate policy rules.

    Args:
        password (str): Candidate password.

    Returns:
        bool: True if the password satisfies all rules.
    """
    if any(c in FORBIDDEN for c in password):
        return False

    straight = any(
        ord(password[i]) + 1 == ord(password[i + 1])
        and ord(password[i + 1]) + 1 == ord(password[i + 2])
        for i in range(len(password) - 2)
    )
    if not straight:
        return False

    pairs = set()
    i = 0
    while i < len(password) - 1:
        if password[i] == password[i + 1]:
            pairs.add(password[i])
            i += 2
        else:
            i += 1
    return len(pairs) >= 2


def next_password(password: str) -> str:
    """Generate the next valid password after the given one.

    Args:
        password (str): Starting password.

    Returns:
        str: First valid password after the given one.
    """
    candidate = increment(password)
    while not is_valid(candidate):
        candidate = increment(candidate)
    return candidate


def solve_part_one(password: str) -> str:
    """Return the next valid password."""
    return next_password(password)


def solve_part_two(password: str) -> str:
    """Return the password that follows the first valid one."""
    return next_password(next_password(password))

def _infer_year_day(path: Path) -> tuple[int | None, int | None]:
    """
    Infer (year, day) from a filename like 'solution_2015_day_01.py'.

    Returns (None, None) if it can't be parsed.
    """
    name = path.name  # e.g. 'solution_2015_day_01.py'

    # Strip extension
    if name.endswith(".py"):
        base = name[:-3]
    else:
        base = name

    parts = base.split("_")
    # Expected: ["solution", "2015", "day", "01"]
    if len(parts) != 4 or parts[0] != "solution" or parts[2] != "day":
        return None, None

    try:
        year = int(parts[1])
        day = int(parts[3])
    except ValueError:
        return None, None

    return year, day

