"""The Halting Problem -- Advent of Code 2017 Day 25."""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Dict, Tuple

Transition = Tuple[int, int, str]  # write, move, next_state


def parse_input(raw: str) -> Tuple[str, int, Dict[str, Dict[int, Transition]]]:
    """Parse the Turing machine description."""
    sections = raw.strip().split("\n\n")
    header = sections[0].splitlines()
    start_state = header[0].split()[-1].strip(".")
    steps = int(header[1].split()[-2])

    states: Dict[str, Dict[int, Transition]] = {}
    for block in sections[1:]:
        lines = [line.strip() for line in block.splitlines() if line.strip()]
        state = lines[0].split()[-1].strip(":")
        transitions: Dict[int, Transition] = {}
        for i in range(1, len(lines), 4):
            value_line = lines[i]
            current_val = int(value_line.split()[-1].strip(":"))
            write_val = int(lines[i + 1].split()[-1].strip("."))
            move_dir = lines[i + 2].split()[-1].strip(".")
            move = 1 if move_dir == "right" else -1
            next_state = lines[i + 3].split()[-1].strip(".")
            transitions[current_val] = (write_val, move, next_state)
        states[state] = transitions
    return start_state, steps, states


def solve_part_one(parsed: Tuple[str, int, Dict[str, Dict[int, Transition]]]) -> int:
    """Diagnostic checksum after given steps."""
    state, steps, transitions = parsed
    tape: Dict[int, int] = {}
    cursor = 0
    for _ in range(steps):
        current = tape.get(cursor, 0)
        write, move, next_state = transitions[state][current]
        tape[cursor] = write
        cursor += move
        state = next_state
    return sum(tape.values())


def solve_part_two(input_data: object) -> str:
    """Return the celebratory message."""
