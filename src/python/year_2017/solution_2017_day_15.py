"""Dueling Generators -- Advent of Code 2017 Day 15."""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Iterator, Tuple

MOD = 2147483647


def parse_input(raw: str) -> Tuple[int, int]:
    """Return starting values for generators A and B."""
    lines = raw.strip().splitlines()
    a = int(lines[0].split()[-1])
    b = int(lines[1].split()[-1])
    return a, b


def generator(start: int, factor: int, multiple: int = 1) -> Iterator[int]:
    """Yield generator values filtered by a multiple constraint."""
    value = start
    while True:
        value = (value * factor) % MOD
        if value % multiple == 0:
            yield value


def judge(a_start: int, b_start: int, pairs: int, multiples: Tuple[int, int]) -> int:
    """Count matches between two generators over a number of pairs."""
    count = 0
    gen_a = generator(a_start, 16807, multiples[0])
    gen_b = generator(b_start, 48271, multiples[1])
    for _ in range(pairs):
        if next(gen_a) & 0xFFFF == next(gen_b) & 0xFFFF:
            count += 1
    return count


def solve_part_one(starts: Tuple[int, int]) -> int:
    """Count matches over 40 million pairs."""
    return judge(starts[0], starts[1], 40_000_000, (1, 1))


def solve_part_two(starts: Tuple[int, int]) -> int:
    """Count matches over 5 million filtered pairs."""
    return judge(starts[0], starts[1], 5_000_000, (4, 8))

def _infer_year_day(path: Path) -> tuple[int | None, int | None]:
    """
    Infer (year, day) from a filename like 'solution_2015_day_01.py'.

    Returns (None, None) if it can't be parsed.
    """
    name = path.name  # e.g. 'solution_2015_day_01.py'

    # Strip extension
    if name.endswith(".py"):
        base = name[:-3]
    else:
        base = name

    parts = base.split("_")
    # Expected: ["solution", "2015", "day", "01"]
    if len(parts) != 4 or parts[0] != "solution" or parts[2] != "day":
        return None, None

    try:
        year = int(parts[1])
        day = int(parts[3])
    except ValueError:
        return None, None

    return year, day

