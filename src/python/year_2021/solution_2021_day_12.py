"""Passage Pathing -- Advent of Code 2021 Day 12."""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Dict, List

Graph = Dict[str, List[str]]


def parse_input(raw: str) -> Graph:
    """
    Build the cave graph from the raw input.

    Args:
        raw: Lines containing cave connections in the form ``a-b``.

    Returns:
        Graph mapping each cave to its neighbors.
    """
    graph: Graph = {}
    for line in raw.strip().splitlines():
        if not line.strip():
            continue
        a, b = line.split("-")
        graph.setdefault(a, []).append(b)
        graph.setdefault(b, []).append(a)
    return graph


def is_small(cave: str) -> bool:
    """
    Check whether a cave name represents a small cave.

    Args:
        cave: Cave identifier.

    Returns:
        True when the cave name is lowercase.
    """
    return cave.islower()


def count_paths(graph: Graph, allow_double: bool) -> int:
    """
    Count all valid paths from start to end following the visit rules.

    Args:
        graph: Cave connections.
        allow_double: Whether one small cave can be visited twice.

    Returns:
        Number of distinct paths from ``start`` to ``end``.
    """

    def dfs(node: str, visited: Dict[str, int], used_double: bool) -> int:
        if node == "end":
            return 1
        total = 0
        for neighbor in graph[node]:
            if neighbor == "start":
                continue
            if is_small(neighbor):
                visits = visited.get(neighbor, 0)
                if visits == 0:
                    visited[neighbor] = 1
                    total += dfs(neighbor, visited, used_double)
                    visited[neighbor] = 0
                elif allow_double and not used_double and visits == 1:
                    visited[neighbor] = 2
                    total += dfs(neighbor, visited, True)
                    visited[neighbor] = 1
            else:
                total += dfs(neighbor, visited, used_double)
        return total

    visited = {"start": 1}
    return dfs("start", visited, False)


def solve_part_one(graph: Graph) -> int:
    """
    Solve Part One with a single visit to each small cave.

    Args:
        graph: Cave connections.

    Returns:
        Total number of valid paths when small caves can be visited only once.
    """
    return count_paths(graph, allow_double=False)


def solve_part_two(graph: Graph) -> int:
    """
    Solve Part Two allowing one small cave to be visited twice.

    Args:
        graph: Cave connections.

    Returns:
        Total number of valid paths with the relaxed rule.
    """
    return count_paths(graph, allow_double=True)

def _infer_year_day(path: Path) -> tuple[int | None, int | None]:
    """
    Infer (year, day) from a filename like 'solution_2015_day_01.py'.

    Returns (None, None) if it can't be parsed.
    """
    name = path.name  # e.g. 'solution_2015_day_01.py'

    # Strip extension
    if name.endswith(".py"):
        base = name[:-3]
    else:
        base = name

    parts = base.split("_")
    # Expected: ["solution", "2015", "day", "01"]
    if len(parts) != 4 or parts[0] != "solution" or parts[2] != "day":
        return None, None

    try:
        year = int(parts[1])
        day = int(parts[3])
    except ValueError:
        return None, None

    return year, day

